import { GOLDEN_BENCHMARKS, PromptBenchmark } from './dataset';
import { AIResearchEvaluator, EvalScorecard, ModelMetrics } from './evaluator';

export interface ExperimentResult {
  benchmarkId: string;
  modelName: string;
  rawResponse: string;
  scorecard: EvalScorecard;
  metrics: ModelMetrics;
  efficiency: number;
}

export interface ExperimentReport {
  timestamp: string;
  totalRuns: number;
  modelsTested: string[];
  results: ExperimentResult[];
  eloLeaderboard: Record<string, number>;
}

/**
 * AI Model Experimentation Runner
 * Simulates batch evaluation of multiple models against target benchmarks.
 */
export class AIResearchExperimentRunner {
  private eloRatings: Record<string, number> = {};

  constructor(initialModels: string[], baseElo: number = 1200) {
    initialModels.forEach(model => {
      this.eloRatings[model] = baseElo;
    });
  }

  /**
   * Evaluates a response generated by a specific model against a prompt benchmark.
   */
  public runSingleTrial(
    benchmark: PromptBenchmark,
    modelName: string,
    response: string,
    metrics: ModelMetrics
  ): ExperimentResult {
    const scorecard = AIResearchEvaluator.evaluateResponse(
      response,
      benchmark.prompt,
      benchmark.personaConfig.vocabulary,
      benchmark.personaConfig.forbiddenWords
    );

    const efficiency = AIResearchEvaluator.calculateEfficiency(metrics);

    // Dynamic ELO updates vs Golden Response (simulated match)
    if (benchmark.goldenResponse) {
      const goldenScore = AIResearchEvaluator.evaluateResponse(
        benchmark.goldenResponse,
        benchmark.prompt,
        benchmark.personaConfig.vocabulary,
        benchmark.personaConfig.forbiddenWords
      );

      // Compare safety and style alignment to calculate outcome score
      const modelFitness = (scorecard.safety + scorecard.styleAlignment) / 2;
      const goldenFitness = (goldenScore.safety + goldenScore.styleAlignment) / 2;

      let outcome = 0.5; // Draw default
      if (modelFitness > goldenFitness + 0.1) outcome = 1.0; // Model outperforms golden standard
      if (modelFitness < goldenFitness - 0.1) outcome = 0.0; // Golden standard wins

      const currentModelElo = this.eloRatings[modelName] || 1200;
      const goldenReferenceElo = 1500; // Reference ELO for absolute golden answer

      const [newModelElo] = AIResearchEvaluator.calculateElo(
        currentModelElo,
        goldenReferenceElo,
        outcome
      );

      this.eloRatings[modelName] = newModelElo;
      scorecard.eloRating = newModelElo;
    }

    return {
      benchmarkId: benchmark.id,
      modelName,
      rawResponse: response,
      scorecard,
      metrics,
      efficiency
    };
  }

  /**
   * Generates a structural research report based on a bundle of trial results.
   */
  public generateReport(results: ExperimentResult[]): ExperimentReport {
    const modelsTested = Array.from(new Set(results.map(r => r.modelName)));

    return {
      timestamp: new Date().toISOString(),
      totalRuns: results.length,
      modelsTested,
      results,
      eloLeaderboard: { ...this.eloRatings }
    };
  }

  public getEloRatings(): Record<string, number> {
    return this.eloRatings;
  }
}
